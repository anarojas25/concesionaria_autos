    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column){
             return false;   
            }
        }; 
        
        
        String titulos[] = {"ID", "Modelo", "Marca", "Color", "Motor", "Placa", "Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List <Auto> listaAutos = controladoraLogica.traerAutos();
        
        if (listaAutos!=null){
            for(Auto auto : listaAutos){
                Object[] objeto = {auto.getId(), auto.getModelo(), auto.getMarca(), auto.getColor(), auto.getMotor(), auto.getPlaca(), auto.getPuertas()};
                
                modeloTabla.addRow(objeto);
                
            }
        }
        
        //este nombre sale de el componente de la GUI
        tablaAutos.setModel(modeloTabla);
    }
